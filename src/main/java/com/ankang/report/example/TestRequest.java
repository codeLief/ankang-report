package com.ankang.report.example;

import java.io.Serializable;

import com.ankang.report.model.ReportRequest;

public class TestRequest extends ReportRequest implements Serializable{
	

	private static final long serialVersionUID = 1L;
	
	private String name;
	private int age;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	@Override
	public String toString() {
		return "TestRequest [name=" + name + ", age=" + age + "]";
	}
}
